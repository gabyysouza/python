raio = int(input("Digite o raio do circulo: "))
pi = 3.14159

diametro = 2 * raio
circuferencia = 2 * pi * raio
area = pi * (raio ** 2)

print("O diametro do circulo é: ", diametro)
print("A circuferencia do circulo é: ", circuferencia)
print("A area do circulo é: ", area)

